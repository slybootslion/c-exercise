void print_block(const int *block, int x, int y, int color) {
  int bx = x;
  for (int i = 0; i < 16; ++i) {
    if (i != 0 && i % 4 == 0) {
      bx = x;
      y += 1;
    }
    if (block[i] == 1) {
      printf("\033[%d;%dH", y, bx);
      printf("\033[37;%dm.`", color);
      printf("\033[0m");
    }
    bx += 2;
  }
}

