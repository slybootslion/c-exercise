//指定位置输出 图形
//n---7图案中的选择某个图案
//m---4个方向中某个方向。
//x,y指定的坐标
//c  颜色
void print_mode_shape(int n, int m, int x, int y, int c) {
    int i = 0;
    int xx = x;
    int yy = y;
    for (i = 0; i < 16; i++) {
        //4 * 4方格，每经过4行,纵坐标 + 1
        if (i != 0 && i % 4 == 0) {
            yy += 1;
            xx = x;
        }

        if (shape[n][m][i] == 1) {
            //指定坐标输出
            printf("\033[%d;%dH", yy, xx);
            //输出颜色[]
            printf("\033[%dm[]", c);
            //关闭颜色
            printf("\033[0m");
        }
        //因此[]占2个位置，所以每次输出坐标偏移2个位置
        xx += 2;
    }
}
